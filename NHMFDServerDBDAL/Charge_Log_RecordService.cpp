DWORD CCharge_Log_RecordService::GetCharge_Log_Record(const _ConnectionPtr &pConnection, const wchar_t *pwchSql, SCharge_Log_Record &sCharge_Log_Record)
{
	assert(NULL != pConnection);
	assert(NULL != pwchSql);

	DWORD dwReturn(0x00);
	_RecordsetPtr pRecordset(NULL);
	if (0x00 != OpenQuery(pConnection, pRecordset, pwchSql))
	{
		// 打开查询失败

		dwReturn = 0xFFFFFFFF;
	}
	else
	{
		// 打开查询成功

		if (!pRecordset->adoEOF)
		{
			FieldToStruct(pRecordset, sCharge_Log_Record);

			dwReturn = 0x01;
		}
		else
		{
			dwReturn = 0x00;
		}
	}

	if (0x00 != CloseQuery(pRecordset))
	{
		// 关闭查询失败

		dwReturn = 0xFFFFFFFF;
	}

	return dwReturn;  
}

DWORD CCharge_Log_RecordService::GetCharge_Log_Record(const _ConnectionPtr &pConnection, const wchar_t *pwchSql, std::list<SCharge_Log_Record> &lsCharge_Log_Record)
{ 
	assert(NULL != pConnection);
	assert(NULL != pwchSql);

	lsCharge_Log_Record.clear();
	DWORD dwReturn(0x00);
	_RecordsetPtr pRecordset(NULL);
	if (0x00 != OpenQuery(pConnection, pRecordset, pwchSql))
	{
		// 打开查询失败

		dwReturn = 0xFFFFFFFF;
	}
	else
	{
		// 打开查询成功

		try
		{
			SCharge_Log_Record sCharge_Log_Record;
			while (!pRecordset->adoEOF)
			{
				FieldToStruct(pRecordset, sCharge_Log_Record);
				lsCharge_Log_Record.push_back(sCharge_Log_Record);
				pRecordset->MoveNext();
			}
			dwReturn = static_cast<DWORD>(lsCharge_Log_Record.size());
		}
		catch (_com_error& e)
		{
			OutputDBErrMsg(e);
			dwReturn = 0xFFFFFFFF;
		}
	}

	if (0x00 != CloseQuery(pRecordset))
	{
		// 关闭查询失败

		dwReturn = 0xFFFFFFFF;
	}

	return dwReturn;  
}

