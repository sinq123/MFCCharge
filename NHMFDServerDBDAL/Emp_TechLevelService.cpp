DWORD CEmp_TechLevelService::GetEmp_TechLevel(const _ConnectionPtr &pConnection, const wchar_t *pwchSql, SEmp_TechLevel &sEmp_TechLevel)
{
	assert(NULL != pConnection);
	assert(NULL != pwchSql);

	DWORD dwReturn(0x00);
	_RecordsetPtr pRecordset(NULL);
	if (0x00 != OpenQuery(pConnection, pRecordset, pwchSql))
	{
		// 打开查询失败

		dwReturn = 0xFFFFFFFF;
	}
	else
	{
		// 打开查询成功

		if (!pRecordset->adoEOF)
		{
			FieldToStruct(pRecordset, sEmp_TechLevel);

			dwReturn = 0x01;
		}
		else
		{
			dwReturn = 0x00;
		}
	}

	if (0x00 != CloseQuery(pRecordset))
	{
		// 关闭查询失败

		dwReturn = 0xFFFFFFFF;
	}

	return dwReturn;  
}

DWORD CEmp_TechLevelService::GetEmp_TechLevel(const _ConnectionPtr &pConnection, const wchar_t *pwchSql, std::list<SEmp_TechLevel> &lsEmp_TechLevel)
{ 
	assert(NULL != pConnection);
	assert(NULL != pwchSql);

	lsEmp_TechLevel.clear();
	DWORD dwReturn(0x00);
	_RecordsetPtr pRecordset(NULL);
	if (0x00 != OpenQuery(pConnection, pRecordset, pwchSql))
	{
		// 打开查询失败

		dwReturn = 0xFFFFFFFF;
	}
	else
	{
		// 打开查询成功

		try
		{
			SEmp_TechLevel sEmp_TechLevel;
			while (!pRecordset->adoEOF)
			{
				FieldToStruct(pRecordset, sEmp_TechLevel);
				lsEmp_TechLevel.push_back(sEmp_TechLevel);
				pRecordset->MoveNext();
			}
			dwReturn = static_cast<DWORD>(lsEmp_TechLevel.size());
		}
		catch (_com_error& e)
		{
			OutputDBErrMsg(e);
			dwReturn = 0xFFFFFFFF;
		}
	}

	if (0x00 != CloseQuery(pRecordset))
	{
		// 关闭查询失败

		dwReturn = 0xFFFFFFFF;
	}

	return dwReturn;  
}

DWORD CEmp_TechLevelService::SetEmp_TechLevel(const _ConnectionPtr &pConnection, const SEmp_TechLevel &sEmp_TechLevel)
{
	assert(NULL != pConnection);

	DWORD dwReturn(0x00);
	wchar_t wchSql[1024] = {L'\0'};
	ZeroMemory(wchSql, sizeof(wchSql));
	_snwprintf_s(wchSql,_countof(wchSql) ,_TRUNCATE, L"SELECT * FROM Emp_TechLevel WHERE Code = '%s'", sEmp_TechLevel.strCode.c_str());
	_RecordsetPtr pRecordset(NULL);
	if (0x00 != OpenQuery(pConnection, pRecordset, wchSql))
	{
		// 打开查询失败

		dwReturn = 0xFFFFFFFF;
	}
	else
	{
		// 打开查询成功

		try
		{
			// 有则改，无则加
			if (pRecordset->adoEOF) 
			{
				pRecordset->AddNew();
			}
			StructToField(pRecordset, sEmp_TechLevel);
			pRecordset->Update();
			dwReturn = 0x01;
		}
		catch (_com_error& e)
		{
			OutputDBErrMsg(e);
			dwReturn = 0xFFFFFFFF;
		}
	}

	if (0x00 != CloseQuery(pRecordset))
	{
		// 关闭查询失败

		dwReturn = 0xFFFFFFFF;
	}

	return dwReturn;  
}

