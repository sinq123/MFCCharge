DWORD CGAVideoRecordService::GetGAVideoRecord(const _ConnectionPtr &pConnection, const wchar_t *pwchSql, SGAVideoRecord &sGAVideoRecord)
{
	assert(NULL != pConnection);
	assert(NULL != pwchSql);

	DWORD dwReturn(0x00);
	_RecordsetPtr pRecordset(NULL);
	if (0x00 != OpenQuery(pConnection, pRecordset, pwchSql))
	{
		// 打开查询失败

		dwReturn = 0xFFFFFFFF;
	}
	else
	{
		// 打开查询成功

		if (!pRecordset->adoEOF)
		{
			FieldToStruct(pRecordset, sGAVideoRecord);

			dwReturn = 0x01;
		}
		else
		{
			dwReturn = 0x00;
		}
	}

	if (0x00 != CloseQuery(pRecordset))
	{
		// 关闭查询失败

		dwReturn = 0xFFFFFFFF;
	}

	return dwReturn;  
}

DWORD CGAVideoRecordService::GetGAVideoRecord(const _ConnectionPtr &pConnection, const wchar_t *pwchSql, std::list<SGAVideoRecord> &lsGAVideoRecord)
{ 
	assert(NULL != pConnection);
	assert(NULL != pwchSql);

	lsGAVideoRecord.clear();
	DWORD dwReturn(0x00);
	_RecordsetPtr pRecordset(NULL);
	if (0x00 != OpenQuery(pConnection, pRecordset, pwchSql))
	{
		// 打开查询失败

		dwReturn = 0xFFFFFFFF;
	}
	else
	{
		// 打开查询成功

		try
		{
			SGAVideoRecord sGAVideoRecord;
			while (!pRecordset->adoEOF)
			{
				FieldToStruct(pRecordset, sGAVideoRecord);
				lsGAVideoRecord.push_back(sGAVideoRecord);
				pRecordset->MoveNext();
			}
			dwReturn = static_cast<DWORD>(lsGAVideoRecord.size());
		}
		catch (_com_error& e)
		{
			OutputDBErrMsg(e);
			dwReturn = 0xFFFFFFFF;
		}
	}

	if (0x00 != CloseQuery(pRecordset))
	{
		// 关闭查询失败

		dwReturn = 0xFFFFFFFF;
	}

	return dwReturn;  
}

