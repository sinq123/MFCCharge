DWORD CLine_DVEDetModeService::GetLine_DVEDetMode(const _ConnectionPtr &pConnection, const wchar_t *pwchSql, SLine_DVEDetMode &sLine_DVEDetMode)
{
	assert(NULL != pConnection);
	assert(NULL != pwchSql);

	DWORD dwReturn(0x00);
	_RecordsetPtr pRecordset(NULL);
	if (0x00 != OpenQuery(pConnection, pRecordset, pwchSql))
	{
		// 打开查询失败

		dwReturn = 0xFFFFFFFF;
	}
	else
	{
		// 打开查询成功

		if (!pRecordset->adoEOF)
		{
			FieldToStruct(pRecordset, sLine_DVEDetMode);

			dwReturn = 0x01;
		}
		else
		{
			dwReturn = 0x00;
		}
	}

	if (0x00 != CloseQuery(pRecordset))
	{
		// 关闭查询失败

		dwReturn = 0xFFFFFFFF;
	}

	return dwReturn;  
}

DWORD CLine_DVEDetModeService::GetLine_DVEDetMode(const _ConnectionPtr &pConnection, const wchar_t *pwchSql, std::list<SLine_DVEDetMode> &lsLine_DVEDetMode)
{ 
	assert(NULL != pConnection);
	assert(NULL != pwchSql);

	lsLine_DVEDetMode.clear();
	DWORD dwReturn(0x00);
	_RecordsetPtr pRecordset(NULL);
	if (0x00 != OpenQuery(pConnection, pRecordset, pwchSql))
	{
		// 打开查询失败

		dwReturn = 0xFFFFFFFF;
	}
	else
	{
		// 打开查询成功

		try
		{
			SLine_DVEDetMode sLine_DVEDetMode;
			while (!pRecordset->adoEOF)
			{
				FieldToStruct(pRecordset, sLine_DVEDetMode);
				lsLine_DVEDetMode.push_back(sLine_DVEDetMode);
				pRecordset->MoveNext();
			}
			dwReturn = static_cast<DWORD>(lsLine_DVEDetMode.size());
		}
		catch (_com_error& e)
		{
			OutputDBErrMsg(e);
			dwReturn = 0xFFFFFFFF;
		}
	}

	if (0x00 != CloseQuery(pRecordset))
	{
		// 关闭查询失败

		dwReturn = 0xFFFFFFFF;
	}

	return dwReturn;  
}

