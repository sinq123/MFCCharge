DWORD CVeh_OilSupModeService::GetVeh_OilSupMode(const _ConnectionPtr &pConnection, const wchar_t *pwchSql, SVeh_OilSupMode &sVeh_OilSupMode)
{
	assert(NULL != pConnection);
	assert(NULL != pwchSql);

	DWORD dwReturn(0x00);
	_RecordsetPtr pRecordset(NULL);
	if (0x00 != OpenQuery(pConnection, pRecordset, pwchSql))
	{
		// 打开查询失败

		dwReturn = 0xFFFFFFFF;
	}
	else
	{
		// 打开查询成功

		if (!pRecordset->adoEOF)
		{
			FieldToStruct(pRecordset, sVeh_OilSupMode);

			dwReturn = 0x01;
		}
		else
		{
			dwReturn = 0x00;
		}
	}

	if (0x00 != CloseQuery(pRecordset))
	{
		// 关闭查询失败

		dwReturn = 0xFFFFFFFF;
	}

	return dwReturn;  
}

DWORD CVeh_OilSupModeService::GetVeh_OilSupMode(const _ConnectionPtr &pConnection, const wchar_t *pwchSql, std::list<SVeh_OilSupMode> &lsVeh_OilSupMode)
{ 
	assert(NULL != pConnection);
	assert(NULL != pwchSql);

	lsVeh_OilSupMode.clear();
	DWORD dwReturn(0x00);
	_RecordsetPtr pRecordset(NULL);
	if (0x00 != OpenQuery(pConnection, pRecordset, pwchSql))
	{
		// 打开查询失败

		dwReturn = 0xFFFFFFFF;
	}
	else
	{
		// 打开查询成功

		try
		{
			SVeh_OilSupMode sVeh_OilSupMode;
			while (!pRecordset->adoEOF)
			{
				FieldToStruct(pRecordset, sVeh_OilSupMode);
				lsVeh_OilSupMode.push_back(sVeh_OilSupMode);
				pRecordset->MoveNext();
			}
			dwReturn = static_cast<DWORD>(lsVeh_OilSupMode.size());
		}
		catch (_com_error& e)
		{
			OutputDBErrMsg(e);
			dwReturn = 0xFFFFFFFF;
		}
	}

	if (0x00 != CloseQuery(pRecordset))
	{
		// 关闭查询失败

		dwReturn = 0xFFFFFFFF;
	}

	return dwReturn;  
}

