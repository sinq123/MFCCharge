DWORD CResultOfFSUNHTService::GetResultOfFSUNHT(const _ConnectionPtr &pConnection, const wchar_t *pwchSql, SResultOfFSUNHT &sResultOfFSUNHT)
{
	assert(NULL != pConnection);
	assert(NULL != pwchSql);

	DWORD dwReturn(0x00);
	_RecordsetPtr pRecordset(NULL);
	if (0x00 != OpenQuery(pConnection, pRecordset, pwchSql))
	{
		// 打开查询失败

		dwReturn = 0xFFFFFFFF;
	}
	else
	{
		// 打开查询成功

		if (!pRecordset->adoEOF)
		{
			FieldToStruct(pRecordset, sResultOfFSUNHT);

			dwReturn = 0x01;
		}
		else
		{
			dwReturn = 0x00;
		}
	}

	if (0x00 != CloseQuery(pRecordset))
	{
		// 关闭查询失败

		dwReturn = 0xFFFFFFFF;
	}

	return dwReturn;
}

DWORD CResultOfFSUNHTService::GetResultOfFSUNHT(const _ConnectionPtr &pConnection, const wchar_t *pwchSql, std::list<SResultOfFSUNHT> &lsResultOfFSUNHT)
{
	assert(NULL != pConnection);
	assert(NULL != pwchSql);

	lsResultOfFSUNHT.clear();
	DWORD dwReturn(0x00);
	_RecordsetPtr pRecordset(NULL);
	if (0x00 != OpenQuery(pConnection, pRecordset, pwchSql))
	{
		// 打开查询失败

		dwReturn = 0xFFFFFFFF;
	}
	else
	{
		// 打开查询成功

		try
		{
			SResultOfFSUNHT sResultOfFSUNHT;
			while (!pRecordset->adoEOF)
			{
				FieldToStruct(pRecordset, sResultOfFSUNHT);
				lsResultOfFSUNHT.push_back(sResultOfFSUNHT);
				pRecordset->MoveNext();
			}
			dwReturn = static_cast<DWORD>(lsResultOfFSUNHT.size());
		}
		catch (_com_error& e)
		{
			OutputDBErrMsg(e);
			dwReturn = 0xFFFFFFFF;
		}
	}

	if (0x00 != CloseQuery(pRecordset))
	{
		// 关闭查询失败

		dwReturn = 0xFFFFFFFF;
	}

	return dwReturn;
}

